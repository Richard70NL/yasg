/************************************************************************************************/

#[derive(Debug)]
pub struct YasgError {
    reasons: Vec<String>,
}

/************************************************************************************************/

impl YasgError {
    /*------------------------------------------------------------------------------------------*/

    pub fn new(reason: String) -> YasgError {
        YasgError {
            reasons: Vec::new(),
        }
        .add_reason(reason)
    }

    /*------------------------------------------------------------------------------------------*/

    pub fn add_reason(mut self, reason: String) -> YasgError {
        self.reasons.insert(0, reason);
        self
    }

    /*------------------------------------------------------------------------------------------*/
}

